let btn = document.querySelector('.btn-1')
let form = document.querySelector('.form')

btn.onclick = () => {
    form.style.display = "block"
    if (form.ondblclick = () => {
        form.style.display = "none"
    }); else {
        form.style.display = "block"

    }
}


